package mybatis.vo;

public class ReservationPointVO {

  private String pointIdx, userIdx, paymentIdx, reviewIdx, pointType, pointSource, pointValue, pointDate, pointStatus;

  public String getPointIdx() {
    return pointIdx;
  }

  public void setPointIdx(String pointIdx) {
    this.pointIdx = pointIdx;
  }

  public String getUserIdx() {
    return userIdx;
  }

  public void setUserIdx(String userIdx) {
    this.userIdx = userIdx;
  }

  public String getPaymentIdx() {
    return paymentIdx;
  }

  public void setPaymentIdx(String paymentIdx) {
    this.paymentIdx = paymentIdx;
  }

  public String getReviewIdx() {
    return reviewIdx;
  }

  public void setReviewIdx(String reviewIdx) {
    this.reviewIdx = reviewIdx;
  }

  public String getPointType() {
    return pointType;
  }

  public void setPointType(String pointType) {
    this.pointType = pointType;
  }

  public String getPointSource() {
    return pointSource;
  }

  public void setPointSource(String pointSource) {
    this.pointSource = pointSource;
  }

  public String getPointValue() {
    return pointValue;
  }

  public void setPointValue(String pointValue) {
    this.pointValue = pointValue;
  }

  public String getPointDate() {
    return pointDate;
  }

  public void setPointDate(String pointDate) {
    this.pointDate = pointDate;
  }

  public String getPointStatus() {
    return pointStatus;
  }

  public void setPointStatus(String pointStatus) {
    this.pointStatus = pointStatus;
  }
}
