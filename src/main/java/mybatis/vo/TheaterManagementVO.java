package mybatis.vo;

public class TheaterManagementVO {

  private String theaterRegion, theaterName, theaterStatus, screenName,
    screenSeatCount, screenType, screenStatus;

  public String getTheaterRegion() {
    return theaterRegion;
  }

  public void setTheaterRegion(String theaterRegion) {
    this.theaterRegion = theaterRegion;
  }

  public String getTheaterName() {
    return theaterName;
  }

  public void setTheaterName(String theaterName) {
    this.theaterName = theaterName;
  }

  public String getTheaterStatus() {
    return theaterStatus;
  }

  public void setTheaterStatus(String theaterStatus) {
    this.theaterStatus = theaterStatus;
  }

  public String getScreenName() {
    return screenName;
  }

  public void setScreenName(String screenName) {
    this.screenName = screenName;
  }

  public String getScreenSeatCount() {
    return screenSeatCount;
  }

  public void setScreenSeatCount(String screenSeatCount) {
    this.screenSeatCount = screenSeatCount;
  }

  public String getScreenType() {
    return screenType;
  }

  public void setScreenType(String screenType) {
    this.screenType = screenType;
  }

  public String getScreenStatus() {
    return screenStatus;
  }

  public void setScreenStatus(String screenStatus) {
    this.screenStatus = screenStatus;
  }
}
