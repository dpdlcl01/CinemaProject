package mybatis.vo;

public class LogVO {
    private String logIdx, logType, adminIdx, logTarget, logInfo, logPreValue, logCurValue, logDate;

    public String getLogIdx() {
        return logIdx;
    }

    public void setLogIdx(String logIdx) {
        this.logIdx = logIdx;
    }

    public String getLogType() {
        return logType;
    }

    public void setLogType(String logType) {
        this.logType = logType;
    }

    public String getAdminIdx() {
        return adminIdx;
    }

    public void setAdminIdx(String adminIdx) {
        this.adminIdx = adminIdx;
    }

    public String getLogTarget() {
        return logTarget;
    }

    public void setLogTarget(String logTarget) {
        this.logTarget = logTarget;
    }

    public String getLogInfo() {
        return logInfo;
    }

    public void setLogInfo(String logInfo) {
        this.logInfo = logInfo;
    }

    public String getLogPreValue() {
        return logPreValue;
    }

    public void setLogPreValue(String logPreValue) {
        this.logPreValue = logPreValue;
    }

    public String getLogCurValue() {
        return logCurValue;
    }

    public void setLogCurValue(String logCurValue) {
        this.logCurValue = logCurValue;
    }

    public String getLogDate() {
        return logDate;
    }

    public void setLogDate(String logDate) {
        this.logDate = logDate;
    }
}
