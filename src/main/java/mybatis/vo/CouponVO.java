package mybatis.vo;

public class CouponVO {
    private String couponIdx, couponName, couponInfo, couponType, couponValue, couponRegDate, couponExpDate, couponStatus, couponUserIdx,
                userIdx, couponUserStatus, couponUserDate;

    public String getCouponIdx() {
        return couponIdx;
    }

    public void setCouponIdx(String couponIdx) {
        this.couponIdx = couponIdx;
    }

    public String getUserIdx() {
        return userIdx;
    }

    public void setUserIdx(String userIdx) {
        this.userIdx = userIdx;
    }

    public String getCouponUserStatus() {
        return couponUserStatus;
    }

    public void setCouponUserStatus(String couponUserStatus) {
        this.couponUserStatus = couponUserStatus;
    }

    public String getCouponUserDate() {
        return couponUserDate;
    }

    public void setCouponUserDate(String couponUserDate) {
        this.couponUserDate = couponUserDate;
    }

    public String getCouponName() {
        return couponName;
    }

    public void setCouponName(String couponName) {
        this.couponName = couponName;
    }

    public String getCouponInfo() {
        return couponInfo;
    }

    public void setCouponInfo(String couponInfo) {
        this.couponInfo = couponInfo;
    }

    public String getCouponType() {
        return couponType;
    }

    public void setCouponType(String couponType) {
        this.couponType = couponType;
    }

    public String getCouponValue() {
        return couponValue;
    }

    public void setCouponValue(String couponValue) {
        this.couponValue = couponValue;
    }

    public String getCouponRegDate() {
        return couponRegDate;
    }

    public void setCouponRegDate(String couponRegDate) {
        this.couponRegDate = couponRegDate;
    }

    public String getCouponExpDate() {
        return couponExpDate;
    }

    public void setCouponExpDate(String couponExpDate) {
        this.couponExpDate = couponExpDate;
    }

    public String getCouponStatus() {
        return couponStatus;
    }

    public void setCouponStatus(String couponStatus) {
        this.couponStatus = couponStatus;
    }

    public String getCouponUserIdx() {
        return couponUserIdx;
    }

    public void setCouponUserIdx(String couponUserIdx) {
        this.couponUserIdx = couponUserIdx;
    }
}
